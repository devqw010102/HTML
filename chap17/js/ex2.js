// 1. 매개변수 없고, 리턴도 없는 함수
function add1() {
    let a = 10;
    let b = 20;

    let result = a + b;
    alert(result);
}

// 2. 매개변수 있고, 리턴은 없는 함수
function add2(a, b) {
    let result = a + b;
    alert(result);
}


// 3. 매개변수 없고, 리턴은 있는 함수
function add3() {
    let a = 10;
    let b = 20;

    return a + b;
}

// 4. 매개변수 있고, 리턴도 있는 함수
function add4(a, b) {
    return a + b;
}


